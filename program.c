#include <stdio.h>
int main()
{
    int a = 7;
    int b = -6;
    int c = 79U;
    long int d = 55557L;
    printf("integer value with positive data: %d\n", a);
    printf("integer value with negative data: %d\n",b);
    printf("integer value with an unsigned int data: %u\n,",c);
    printf("integer value with an long int data: %1d", d);
    return 0;





}